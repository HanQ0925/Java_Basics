
public class variable {
	public static void main(String[] args) {
		int year=0;
		int age =14;
		
		System.out.println(year);
		System.out.println(age);
		
		year = year+2000;
		age = age+1;
		System.out.printf("finger:[%5d]%n", 10); //우측정렬
		System.out.printf("finger:[%-5d]%n", 10); //좌측정렬
		System.out.printf("finger:[%05d]%n", 10);
	}
}
